    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            if (row != -1) {
                String materialID = jTable1.getValueAt(row, 0).toString();
                String materialName = jTable1.getValueAt(row, 1).toString();
                String materialType = jTable1.getValueAt(row, 3).toString();
                
                p.materialBean = new StockMaterialBean(materialID, materialName, materialType);
                dispose();

            }else{
            p.materialBean = null;
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

