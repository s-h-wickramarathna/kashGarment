    private void loadMaterialTable(String text) {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            ResultSet resultSet = MYSQL.Search("SELECT *\n"
                    + "FROM `material`\n"
                    + "INNER JOIN `material_type` ON `material_type`.`mt_id`=`material`.`material_type_mt_id`\n"
                    + "INNER JOIN `material_color` ON `material_color`.`material_m_id`=`material`.`m_id`\n"
                    + "INNER JOIN `color` ON `color`.`c_id`=`material_color`.`color_c_id` "
                    + "WHERE `m_id` LIKE '" + text + "%' OR `m_name` LIKE '" + text + "%' ");
            while (resultSet.next()) {
                String m_id = resultSet.getString("m_id");
                String m_name = resultSet.getString("m_name");
                String m_desc = resultSet.getString("m_description");
                String mt_name = resultSet.getString("mt_name");
                String m_color = resultSet.getString("c_name");
                
                Vector<String> vector = new Vector<String>();
                vector.add(m_id);
                vector.add(m_name);
                vector.add(m_desc);
                vector.add(mt_name);
                vector.add(m_color);
                model.addRow(vector);
            }
            
        } catch (Exception e) {
             logger.warning(String.valueOf(e));
        }
    }


