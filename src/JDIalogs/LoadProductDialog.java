    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();

        if (evt.getClickCount() == 1) {

            String pname = String.valueOf(jTable1.getValueAt(selectedRow, 0));
            jTextField5.setText(pname);

            String color = String.valueOf(jTable1.getValueAt(selectedRow, 1));
            jComboBox3.setSelectedItem(color);

            String size = String.valueOf(jTable1.getValueAt(selectedRow, 2));
            jComboBox4.setSelectedItem(size);

            String cemail = String.valueOf(jTable1.getValueAt(selectedRow, 3));
            jTextField8.setText(cemail);

            try {
                ResultSet rs = MYSQL.Search("SELECT * FROM `product` WHERE `p_name`='" + pname + "'");

                if (rs.next()) {

                    jTextArea3.setText(rs.getString("p_description"));

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

        if (evt.getClickCount() == 2) {

            try {

                String pname = String.valueOf(jTable1.getValueAt(selectedRow, 0));

                ResultSet rs = MYSQL.Search("SELECT * FROM `product` WHERE `p_name`='" + pname + "'");

               if(rs.next()){
               
                    String product = rs.getString("p_id");
                p.product = product;
                this.dispose();
                   
               }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

