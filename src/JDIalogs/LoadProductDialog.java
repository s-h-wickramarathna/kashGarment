    public void loadColor() {

        try {

            ResultSet rs = MYSQL.Search("SELECT * FROM `color`");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {

                v.add(rs.getString("c_name"));
                this.colorMap.put(rs.getString("c_name"), rs.getInt("c_id"));

            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);
            jComboBox3.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

