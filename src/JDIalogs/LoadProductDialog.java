    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:

        String pname = jTextField1.getText();
        String color = String.valueOf(jComboBox1.getSelectedItem());
        String size = String.valueOf(jComboBox2.getSelectedItem());
        String cemail = jTextField4.getText();
        String desc = jTextArea2.getText();

        if (pname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (color.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Color", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (size.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Size", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (cemail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select Client Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Product Description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

//          
                boolean isFound = false;

                for (int i = 0; i < jTable1.getRowCount(); i++) {

                    String getPName = String.valueOf(jTable1.getValueAt(i, 0));
                    String getColor = String.valueOf(jTable1.getValueAt(i, 1));
                    String getSize = String.valueOf(jTable1.getValueAt(i, 2));
                    String getClientEmail = String.valueOf(jTable1.getValueAt(i, 3));

                    if (pname.equals(getPName) && color.equals(getColor) && size.equals(getSize) && cemail.equals(getClientEmail)) {

                        JOptionPane.showMessageDialog(this, "Product Already Added", "Warning", JOptionPane.WARNING_MESSAGE);
                        isFound = true;
                        break;

                    }

                }

                if (!isFound) {

                    try {

                        MYSQL.Iud("INSERT INTO `product`(`p_name`,`p_description`,`client_cl_email`) "
                                + "VALUES('" + pname + "','" + desc + "','" + cemail + "')");

                        ResultSet rs = MYSQL.Search("SELECT * FROM `product` WHERE `p_name`='" + pname + "'");

                        if (rs.next()) {

                            MYSQL.Iud("INSERT INTO `product_color`(`product_p_id`,`color_c_id`) VALUES('" + rs.getString("p_id") + "','" + colorMap.get(color) + "')");
//
                            MYSQL.Iud("INSERT INTO `product_size`(`product_p_id`,`size_s_id`) VALUES('" + rs.getString("p_id") + "','" + sizeMap.get(size) + "')");
                            loadProduct("");
                            reset();

                        }

                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton7ActionPerformed

