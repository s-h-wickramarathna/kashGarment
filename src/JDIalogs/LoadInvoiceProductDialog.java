    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();

            if (row != -1) {
                String sid = jTable1.getValueAt(row, 0).toString();
                String id = jTable1.getValueAt(row, 1).toString();
                String name = jTable1.getValueAt(row, 2).toString();
                String description = jTable1.getValueAt(row, 3).toString();
                String color = jTable1.getValueAt(row, 4).toString();
                String start = jTable1.getValueAt(row, 5).toString();
                String end = jTable1.getValueAt(row, 6).toString();
                String quentity = jTable1.getValueAt(row, 7).toString();
                String unitPrice = jTable1.getValueAt(row, 8).toString();
                String damage = jTable1.getValueAt(row, 9).toString();
                invoicePanel.productBean = new InvoiceProductBean(sid, id, name, description, color, start, end, quentity, unitPrice, damage);

                this.dispose();

            } else {
                invoicePanel.productBean = null;
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

