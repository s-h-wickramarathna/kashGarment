    private void loadProduct(String text, String email) {

        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `product`\n"
                    + "INNER JOIN `product_details` ON `product_details`.`product_p_id`=`product`.`p_id`\n"
                    + "INNER JOIN `product_color` ON `product_color`.`product_p_id`=`product`.`p_id`\n"
                    + "INNER JOIN `color` ON `color`.`c_id`=`product_color`.`color_c_id`\n"
                    + "WHERE `product`.`client_cl_email`='" + email + "' "
                    + "AND (`p_id` LIKE '" + text + "%' OR `p_name` LIKE '" + text + "%') "
                    + "AND `shipping_status_ship_id`='1' ");

            while (resultSet.next()) {
                String sid = resultSet.getString("pd_id");
                String id = resultSet.getString("p_id");
                String name = resultSet.getString("p_name");
                String description = resultSet.getString("p_description");
                String color = resultSet.getString("c_name");
                String start = resultSet.getString("start_date");
                String end = resultSet.getString("end_date");
                String quentity = resultSet.getString("pd_qty");
                String unitPrice = resultSet.getString("pd_unit_price");
                String damage = resultSet.getString("pd_damage_items");

                Vector v = new Vector();
                v.add(sid);
                v.add(id);
                v.add(name);
                v.add(description);
                v.add(color);
                v.add(start);
                v.add(end);
                v.add(quentity);
                v.add(unitPrice);
                v.add(damage);
                model.addRow(v);

            }

        } catch (Exception e) {
             logger.warning(String.valueOf(e));
        }
    }

