    private void loadCompany() {
        try {

            ResultSet resultSet = MYSQL.Search("SELECT * FROM `company`");
            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("com_name"));
                companyMap.put(resultSet.getString("com_name"), resultSet.getString("com_id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private String validateFields() {

        if (jTextField2.getText().isEmpty()) {
            return "Please Enter First Name";

        } else if (jTextField3.getText().isEmpty()) {
            return "Please Enter Last Name";

        } else if (jTextField1.getText().isEmpty()) {
            return "Please Email Address";

        } else if (!EmailValidate.validateEmail(jTextField1.getText())) {
            return "Invalid email address";

        } else if (jTextField4.getText().isEmpty()) {
            return "Please Enter Mobile Number";

        } else if (jComboBox1.getSelectedIndex() == 0) {
            return "Please Select Company";

        } else {
            return "";

        }
    }

