    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String validate = validateFields();
        if (validate.isEmpty()) {

            try {
                ResultSet resultSet = MYSQL.Search("SELECT * FROM `supplier` WHERE `sup_email`='" + jTextField1.getText() + "' ");

                if (!resultSet.next()) {

                    MYSQL.Iud("INSERT INTO `supplier`(`sup_fname`,`sup_lname`,`sup_mobile`,`sup_email`,`company_com_id`) "
                            + "VALUES('" + jTextField2.getText() + "',"
                            + "'" + jTextField3.getText() + "',"
                            + "'" + jTextField4.getText() + "',"
                            + "'" + jTextField1.getText() + "',"
                            + "'" + companyMap.get(jComboBox1.getSelectedItem().toString()) + "') ");

                    validateFields();
                    loadSupplierTable("");

                } else {
                    JOptionPane.showMessageDialog(this, "This Supplier Already Exist", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                logger.warning(String.valueOf(e));
            }
        } else {
            JOptionPane.showMessageDialog(this, validate, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

