    private void loadSupplierTable(String text) {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `supplier` "
                    + "INNER JOIN `company` ON `company`.`com_id`=`supplier`.`company_com_id` "
                    + "WHERE `sup_email` LIKE '" + text + "%'");

            while (resultSet.next()) {
                Vector v = new Vector();
                v.add(resultSet.getString("sup_id"));
                v.add(resultSet.getString("sup_email"));
                v.add(resultSet.getString("sup_fname") + " " + resultSet.getString("sup_lname"));
                v.add(resultSet.getString("sup_mobile"));
                v.add(resultSet.getString("com_name"));
                model.addRow(v);
            }

        } catch (Exception e) {
            logger.warning(String.valueOf(e));
        }
    }

