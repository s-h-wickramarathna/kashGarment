    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {

            int row = jTable1.getSelectedRow();

            if (row != -1) {

                String email = jTable1.getValueAt(row, 0).toString();
                String fname = jTable1.getValueAt(row, 1).toString();
                String lname = jTable1.getValueAt(row, 2).toString();
                String mobile = jTable1.getValueAt(row, 3).toString();

                invoiceManagePanel.clientBean = new ClientBean(email, fname, lname, mobile);

                this.dispose();

            } else {
                invoiceManagePanel.clientBean = null;
            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

