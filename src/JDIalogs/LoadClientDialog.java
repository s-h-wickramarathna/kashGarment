    private void loadClient(String text) {
        try {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `client` WHERE `cl_email` LIKE '" + text + "%' ");

            while (resultSet.next()) {
                String fname = resultSet.getString("cl_fname");
                String lname = resultSet.getString("cl_lname");
                String email = resultSet.getString("cl_email");
                String mobile = resultSet.getString("cl_mobile");

                Vector v = new Vector();
                v.add(email);
                v.add(fname);
                v.add(lname);
                v.add(mobile);

                model.addRow(v);
            }

        } catch (Exception e) {
             logger.warning(String.valueOf(e));
        }
    }

