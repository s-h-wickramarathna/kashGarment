    public void loadProductDetail(String search) {
        
        try {
            
            String query = "SELECT * FROM `product` INNER JOIN `product_details` INNER JOIN `shipping_status`"
                    + "ON `product`.`p_id`=`product_details`.`product_p_id` AND `shipping_status`.`ship_id`=`product_details`.`shipping_status_ship_id`";
            
            if (search.isEmpty()) {
                query += "";
            } else {
                query += "WHERE `p_name` LIKE '" + search + "' OR `pd_id` LIKE '" + search + "'";
            }
            
            ResultSet rs = MYSQL.Search(query);
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            while (rs.next()) {
                
                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("pd_id"));
                vector.add(rs.getString("product.p_name"));
                vector.add(rs.getString("start_date"));
                vector.add(rs.getString("end_date"));
                vector.add(rs.getString("shipping_date"));
                vector.add(rs.getString("shipping_status.ship_name"));
                vector.add(rs.getString("pd_qty"));
                vector.add(rs.getString("pd_unit_price"));
                vector.add(rs.getString("pd_damage_items"));
                model.addRow(vector);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
    public String generateUniqueId() {
        String text = String.valueOf(UUID.randomUUID());
        String[] results = text.split("(-)");
        String id = "#" + results[0];
        return id;
        
    }
    
    public void loadStatus() {
        
        try {
            
            ResultSet rs = MYSQL.Search("SELECT * FROM `shipping_status`");
            
            Vector v = new Vector();
            v.add("Select");
            
            while (rs.next()) {
                
                v.add(rs.getString("ship_name"));
                this.statusMap.put(rs.getString("ship_name"), rs.getInt("ship_id"));
                
            }
            
            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);
            jComboBox3.setModel(model);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }
    
