    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        int selectedRow = jTable1.getSelectedRow();
        
        if (evt.getClickCount() == 1) {
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            try {
                
                String pname = String.valueOf(jTable1.getValueAt(selectedRow, 1));
                ResultSet rs = MYSQL.Search("SELECT * FROM `product` WHERE `p_name`='" + pname + "'");
                
                if (rs.next()) {
                    
                    jLabel13.setText(rs.getString("p_id"));
                    
                }
                
                Date sdate = sdf.parse(String.valueOf(jTable1.getValueAt(selectedRow, 2)));
                jDateChooser4.setDate(sdate);
                
                Date edate = sdf.parse(String.valueOf(jTable1.getValueAt(selectedRow, 3)));
                jDateChooser5.setDate(edate);
                
                Date shdate = sdf.parse(String.valueOf(jTable1.getValueAt(selectedRow, 4)));
                jDateChooser6.setDate(shdate);
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            String status = String.valueOf(jTable1.getValueAt(selectedRow, 5));
            jComboBox3.setSelectedItem(status);
            
            String qty = String.valueOf(jTable1.getValueAt(selectedRow, 6));
            jTextField4.setText(qty);
            
            String uprice = String.valueOf(jTable1.getValueAt(selectedRow, 7));
            jTextField5.setText(uprice);
            
            String ditems = String.valueOf(jTable1.getValueAt(selectedRow, 8));
            jTextField6.setText(ditems);
            
        }

    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        jLabel13.disable();
        jButton3.disable();
        
        String pid = jLabel13.getText();
        Date sdate = jDateChooser4.getDate();
        Date edate = jDateChooser5.getDate();
        Date shdate = jDateChooser6.getDate();
        String status = String.valueOf(jComboBox3.getSelectedItem());
        String qty = jTextField4.getText();
        String uprice = jTextField5.getText();
        String ditems = jTextField6.getText();
        
        if (pid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select Product Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (sdate == null) {
            JOptionPane.showMessageDialog(this, "Please Enter Start Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (edate == null) {
            JOptionPane.showMessageDialog(this, "Please Enter End Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (sdate.after(edate)) {
            JOptionPane.showMessageDialog(this, "Start Date greater than End Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (shdate == null) {
            JOptionPane.showMessageDialog(this, "Please Enter Shipping Date", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Status", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (qty.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Qty", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (uprice.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Unit Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (ditems.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Damager Items", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            
            try {

                //
                boolean isFound = false;
                
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    
                    String getPdID = String.valueOf(jTable1.getValueAt(i, 0));
                    String getPid = String.valueOf(jTable1.getValueAt(i, 1));
                    String getsDate = String.valueOf(jTable1.getValueAt(i, 2));
                    String geteDate = String.valueOf(jTable1.getValueAt(i, 3));
                    String getshDate = String.valueOf(jTable1.getValueAt(i, 4));
                    String getstatus = String.valueOf(jTable1.getValueAt(i, 5));
                    String getqty = String.valueOf(jTable1.getValueAt(i, 6));
                    String getuPrice = String.valueOf(jTable1.getValueAt(i, 7));
                    String getdItems = String.valueOf(jTable1.getValueAt(i, 8));
                    
                    ResultSet rs = MYSQL.Search("SELECT * FROM `product_details` WHERE `product_p_id`='" + pid + "'");
                    
                    if (rs.next()) {
                        
                        if (getPdID.equals(rs.getString("pd_id")) && pid.equals(getPid) && sdate.equals(getsDate) && edate.equals(geteDate) && shdate.equals(getshDate) && status.equals(getstatus) && qty.equals(getqty) && uprice.equals(getuPrice) && ditems.equals(getdItems)) {
                            
                            JOptionPane.showMessageDialog(this, "Product Detail Already Added", "Warning", JOptionPane.WARNING_MESSAGE);
                            isFound = true;
                            break;
                            
                        }
                        
                    }
                    
                }
                
                if (!isFound) {
                    
                    try {
                        
                        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                        String s = sdf.format(sdate);
                        String e = sdf.format(edate);
                        String sh = sdf.format(shdate);
                        
                        ResultSet rs = MYSQL.Search("SELECT * FROM `product_details`WHERE `product_p_id`='" + pid + "'");
                        
                        if (rs.next()) {
                            
                            MYSQL.Iud("UPDATE `product_details` SET `start_date`='" + s + "',`end_date`='" + e + "',`shipping_date`='" + sh + "',`shipping_status_ship_id`='" + statusMap.get(status) + "',`pd_qty`='" + qty + "',`pd_unit_price`='" + uprice + "',`pd_damage_items`='" + ditems + "' "
                                    + "WHERE `pd_id`='" + rs.getString("pd_id") + "' AND `product_p_id`='" + pid + "'");
                            
                            loadProductDetail("");
                            reset();
                            
                        }
                        
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                    
                }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

