    public void loadProductState(String search) {

        try {

            String query = "SELECT * FROM `product_state` INNER JOIN `product_details` INNER JOIN `state` INNER JOIN `product`"
                    + "ON `state`.`st_id` = `product_state`.`state_st_id` AND `product_details`.`pd_id` = `product_state`.`product_details_pd_id`"
                    + "AND `product`.`p_id`=`product_details`.`product_p_id`";

            if (search.isEmpty()) {
                query += "";
            } else {
                query += "WHERE `p_name` LIKE '" + search + "'";
            }

            ResultSet rs = MYSQL.Search(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("ps_id"));
                vector.add(rs.getString("state.st_name"));
                vector.add(rs.getString("product.p_name"));

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadState() {

        try {

            ResultSet rs = MYSQL.Search("SELECT * FROM `state`");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {

                v.add(rs.getString("st_name"));
                this.stateMap.put(rs.getString("st_name"), rs.getInt("st_id"));

            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox1.setModel(model);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

