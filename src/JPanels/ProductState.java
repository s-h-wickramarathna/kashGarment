    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String pdid = jLabel3.getText();
        String state = String.valueOf(jComboBox1.getSelectedItem());

        if (state.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select State", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (pdid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Select Product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MYSQL.Search("SELECT * FROM `product_state` WHERE `state_st_id`='" + stateMap.get(state) + "' AND `product_details_pd_id`='" + pdid + "'");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Product Already Added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MYSQL.Iud("INSERT INTO `Product_state`(`state_st_id`,`product_details_pd_id`)VALUES('" + stateMap.get(state) + "','" + pdid + "')");

                    loadProductState("");
//                    reset();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton3ActionPerformed

