    private void load_material(String text) {

        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        try {

            ResultSet resultSet = MYSQL.Search("SELECT * FROM `material`"
                    + " INNER JOIN `material_type` ON `material`.`material_type_mt_id` = `material_type`.`mt_id`"
                    + "INNER JOIN `material_color` ON `material`.`m_id` = `material_color`.`material_m_id` "
                    + "INNER JOIN `color` ON `color`.`c_id` = `material_color`.`color_c_id` WHERE `m_name` LIKE '" + text + "%'");

            while (resultSet.next()) {
                Vector<String> v = new Vector<String>();
                v.add(resultSet.getString("m_id"));
                v.add(resultSet.getString("m_name"));
                v.add(resultSet.getString("m_description"));
                v.add(resultSet.getString("mt_name"));
                v.add(resultSet.getString("c_name"));

                model.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

