    public void load_materialType() {

        try {
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `material_type`");

            Vector v = new Vector();
            v.add("Select");

            while (resultSet.next()) {
                v.add(resultSet.getString("mt_name"));
                materialTypeMap.put(resultSet.getString("mt_name"), resultSet.getInt("mt_id"));

            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(v);
            jComboBox5.setModel(model);
            jComboBox7.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

