    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable2.getSelectedRow() == -1) {

            JOptionPane.showMessageDialog(this, "Please Select A Row", "Error", JOptionPane.ERROR_MESSAGE);

        } else {

            String id = jTextField2.getText();
            String name = jTextField5.getText();
            String description = jTextField6.getText();
            String type = String.valueOf(jComboBox7.getSelectedItem());
            String color = String.valueOf(jComboBox8.getSelectedItem());

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Material Name", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (description.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter the Description", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (type.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select the Material Type", "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (color.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select the Material Color", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                int typeId = materialTypeMap.get(type);
                int colorId = materialColorMap.get(color);

                try {
                    ResultSet resultSet = MYSQL.Search("SELECT * FROM `material` WHERE `m_id`!='" + id + "' AND `m_name`='" + name + "' ");

                    if (!resultSet.next()) {

                        MYSQL.Iud("UPDATE `material` SET `m_name`='" + name + "',`m_description`='" + description + "', `material_type_mt_id` = '" + typeId + "' "
                                + "WHERE `m_id`='" + id + "'");

                        MYSQL.Iud("UPDATE `material_color` SET `color_c_id` = '" + colorId + "' WHERE `material_m_id` = '" + jTable2.getValueAt(jTable2.getSelectedRow(), 0) + "'");

                        load_material("");
                        JOptionPane.showMessageDialog(this, "Entered Date Successfully", "Success", JOptionPane.PLAIN_MESSAGE);
                        reset();

                    } else {
                        JOptionPane.showMessageDialog(this, "This Material Already Exist.", "Error", JOptionPane.ERROR_MESSAGE);
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

