    private void loadStockTable(String text) {
        
        try {
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
            ResultSet resultSet = MYSQL.Search("SELECT * FROM `stock` "
                    + "INNER JOIN `material` ON `stock`.`material_m_id`=`material`.`m_id` "
                    + "INNER JOIN `material_type` ON `material_type`.`mt_id`=`material`.`material_type_mt_id`"
                    + "WHERE `stock_id` LIKE '" + text + "%' OR `m_name` LIKE '" + text + "%' ORDER BY `stock_date` ASC");
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<String>();
                vector.add(resultSet.getString("stock_id"));
                vector.add(resultSet.getString("material.m_id"));
                vector.add(resultSet.getString("m_name"));
                vector.add(resultSet.getString("material_type.mt_name"));
                vector.add(resultSet.getString("stock_qty"));
                vector.add(resultSet.getString("stock_unitPrice"));
                vector.add(resultSet.getString("stock_date"));
                vector.add(resultSet.getString("stock_damage_item"));
                model.addRow(vector);
            }
            
        } catch (Exception e) {
             logger.warning(String.valueOf(e));
        }
        
    }

