    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String validate = validateAddStockFields();
        
        if (validate.isEmpty()) {
            String m_id = jTextField1.getText();
            String qty = jTextField5.getText();
            String unitPrice = jTextField6.getText();
            String date = jTextField4.getText();
            String damage = jTextField7.getText();
            
            MYSQL.Iud("INSERT INTO `stock`(`material_m_id`,`stock_qty`,`stock_unitPrice`,`stock_date`,`stock_damage_item`) "
                    + "VALUES('" + m_id + "','" + qty + "','" + unitPrice + "','" + date + "','" + damage + "')");
            
            resetAddStockFields();
            loadStockTable("");
            
        } else {
            JOptionPane.showMessageDialog(this, validate, "Error", JOptionPane.ERROR_MESSAGE);
            
        }

    }//GEN-LAST:event_jButton3ActionPerformed

