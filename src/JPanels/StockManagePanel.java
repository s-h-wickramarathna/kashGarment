    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            
            if (row != -1) {
                jTabbedPane1.setSelectedIndex(1);
                
                String stockID = jTable1.getValueAt(row, 0).toString();
                String material = jTable1.getValueAt(row, 1).toString();
                String name = jTable1.getValueAt(row, 2).toString();
                String type = jTable1.getValueAt(row, 3).toString();
                String qty = jTable1.getValueAt(row, 4).toString();
                String unitPrice = jTable1.getValueAt(row, 5).toString();
                String damage = jTable1.getValueAt(row, 7).toString();
                
                jTextField9.setText(stockID);
                jTextField15.setText(material);
                jTextField10.setText(name);
                jTextField11.setText(type);
                jTextField12.setText(qty);
                jTextField13.setText(unitPrice);
                jTextField14.setText(damage);
                
                jTable1.setEnabled(false);
                jTextField12.setEnabled(true);
                jTextField13.setEnabled(true);
                jTextField14.setEnabled(true);
                jButton5.setEnabled(true);
                jButton4.setEnabled(true);
                
            } else {
                jTable1.setEnabled(true);
                jTextField12.setEnabled(false);
                jTextField13.setEnabled(false);
                jTextField14.setEnabled(false);
                jButton5.setEnabled(false);
                jButton4.setEnabled(false);
                jTable1.clearSelection();
                
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

